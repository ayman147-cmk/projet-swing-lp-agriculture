    private void btnModifierActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModifierActionPerformed
  int row = jTable1.getSelectedRow();
    if (row != -1) {
        try {
            int idVente = Integer.parseInt(jTable1.getValueAt(row, 0).toString());
            
            String nomAgri = jComboBox1.getSelectedItem().toString();
            String nomProd = jComboBox2.getSelectedItem().toString();
            
            int idAgri = 0;
            for (Agriculteur a : new AgriculteurService().findAll()) {
                if (a.getNom().equals(nomAgri)) { idAgri = a.getId(); break; }
            }
            
            int idProd = 0;
            for (ProduitAgro p : new ProduitService().findAll()) {
                if (p.getNom().equals(nomProd)) { idProd = p.getId(); break; }
            }

            double qte = Double.parseDouble(jTextField1.getText().trim());
            java.sql.Date sqlDate = new java.sql.Date(jDateChooser1.getDate().getTime());

            VenteAgro v = new VenteAgro(idVente, String.valueOf(idProd), String.valueOf(idAgri), sqlDate, qte);
            
            if (vs.update(v)) {
                JOptionPane.showMessageDialog(this, "La modification a √©t√© effectu√©e avec succ√®s.");
                loadTable();
            } else {
                JOptionPane.showMessageDialog(this, "√âchec de la modification dans la base de donn√©es.");
            }
            
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Erreur: Veuillez v√©rifier que les valeurs saisies sont des nombres valides.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Erreur: " + e.getMessage());
        }
    } else {
        JOptionPane.showMessageDialog(this, "Veuillez s√©lectionner une ligne du tableau afin de la modifier.");
    }
    }//GEN-LAST:event_btnModifierActionPerformed

